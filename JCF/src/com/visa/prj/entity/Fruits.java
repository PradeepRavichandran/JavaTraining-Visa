package com.visa.prj.entity;

public class Fruits {
	private String name;
	private Integer count;
	public Fruits(String name, Integer count) {
		super();
		this.name = name;
		this.count = count;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getCount() {
		return count;
	}
	public void setCount(Integer count) {
		this.count = count;
	}
	

}
